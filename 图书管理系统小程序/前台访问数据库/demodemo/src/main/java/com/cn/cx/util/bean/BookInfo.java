package com.cn.cx.util.bean;
public class BookInfo {
    private int xuhao;
    private String bookname;
    private int zongliang;
    private int yuliang;
    private String sj_number;
    private String leixing;
    private String  image;
    public int getXuhao() {
        return xuhao;
    }
    public void setXuhao(int xuhao) {
        this.xuhao = xuhao;
    }
    public String getBookname() {
        return bookname;
    }
    public void setBookname(String bookname) {
        this.bookname = bookname;
    }
    public int getZongliang() {
        return zongliang;
    }
    public void setZongliang(int zongliang) {
        this.zongliang = zongliang;
    }
    public int getYuliang() {
        return yuliang;
    }
    public void setYuliang(int yuliang) {
        this.yuliang = yuliang;
    }
    public void setSj_number(String sj_number) {
        this.sj_number = sj_number;
    }
    public String getSj_number() {
        return sj_number;
    }
    public String getLeixing() {
        return leixing;
    }
    public void setLeixing(String leixing) {
        this.leixing = leixing;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getImage() {
        return image;
    }
}
